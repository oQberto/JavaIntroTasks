package by.jonline.module1.branching.task3;

public class CreatePoint {

    private final double x;
    private final double y;

    CreatePoint (double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
