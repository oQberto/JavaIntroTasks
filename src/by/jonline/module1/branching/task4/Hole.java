package by.jonline.module1.branching.task4;

public class Hole {

    private final int length;
    private final int height;

    Hole (int length, int height) {
        this.length = length;
        this.height = height;
    }

    public int getLength() {
        return length;
    }

    public int getHeight() {
        return height;
    }
}